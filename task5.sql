--INNER JOIN

SELECT E.EMPNO, E.ENAME, E.JOB, D.DNAME, D.LOCATION
FROM EMPLOYEES E
INNER JOIN DEPT D ON E.DEPTNO = D.DEPTNO;

--LEFT JOIN

SELECT E.EMPNO, E.ENAME, E.DEPTNO, D.DNAME
FROM EMPLOYEES E
LEFT JOIN DEPT D ON E.DEPTNO = D.DEPTNO;

--RIGHT JOIN

SELECT E.EMPNO, E.ENAME, E.DEPTNO, D.DNAME
FROM EMPLOYEES E
RIGHT JOIN DEPT D ON E.DEPTNO = D.DEPTNO;

